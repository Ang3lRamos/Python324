import math as m

print(m.floor(4.99))#redondea hacia abajo
print(m.ceil(4.01))#redondea hacia arriba
n = [0.999,1,2,3]
print(m.fsum(n))#retorna la suma de la lista
print(m.trunc(45.3))#despues del decimal lo ignora
print(m.pow(2,4))#potencia, 1er: numero 2do: elevado
print(m.sqrt(12))#retorna el resultado de la raiz cuadrada
print(m.pi)#retorna el valor de pi
print(m.e)#retorna el valor de euler
