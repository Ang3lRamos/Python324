#multiples condiciones evaluando si un caracter es vocal 
car = input("Digite un caracter: ")

if car == "a" or car == "A":
    print(f"{car} es vocal")
elif car == "e" or car == "E":
    print(f"{car} es vocal")
elif car == "i" or car == "I":
    print(f"{car} es vocal")
elif car == "o" or car == "O":
    print(f"{car} es vocal")
elif car == "u" or car == "U":
    print(f"{car} es vocal")
else:
    print(f"{car} no es vocal")
