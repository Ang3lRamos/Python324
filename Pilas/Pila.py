#El concepto de pila se refiere a un elemento el cual si entra de ultimo sale de primera
#Para simular una pila creare una lista y usare los metodos append y pop

lista_pila = [17,1.76,"Angel","Ingeniero"]

lista_pila.append("5to semestre")

print(lista_pila)

lista_pila.pop()

print(lista_pila)

#Usando estos dos metodos se simula el concepto de pila
